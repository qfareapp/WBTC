const LanguageContext = createContext(null);

const applyVars = (text, vars = {}) =>
  Object.entries(vars).reduce((result, [key, value]) => result.replace(`{${key}}`, String(value)), text);

export function AppLanguageProvider({ children }) {
  const [language, setLanguage] = useState("en");

  useEffect(() => {
    const loadLanguage = async () => {
      const stored = await AsyncStorage.getItem(LANGUAGE_KEY);
      if (stored === "bn" || stored === "en") setLanguage(stored);
    };
    loadLanguage();
  }, []);

  const updateLanguage = useCallback(async (nextLanguage) => {
    setLanguage(nextLanguage);
    await AsyncStorage.setItem(LANGUAGE_KEY, nextLanguage);
  }, []);

  const toggleLanguage = useCallback(async () => {
    await updateLanguage(language === "en" ? "bn" : "en");
  }, [language, updateLanguage]);

  const t = useCallback((section, key, vars) => {
    const pack = translations[language]?.[section] || {};
    const fallback = translations.en?.[section] || {};
    const value = pack[key] ?? fallback[key] ?? key;
    return typeof value === "string" ? applyVars(value, vars) : value;
  }, [language]);

  const value = useMemo(
    () => ({ language, setLanguage: updateLanguage, toggleLanguage, t }),
    [language, t, toggleLanguage, updateLanguage]
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export const useAppLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) throw new Error("useAppLanguage must be used within AppLanguageProvider");
  return context;
};
